package com.java.oops;

public class Student {
    // The name of the student
    String name;

    // The roll number assigned to the student
    int rollNo;

    // The age of the student
    int age;

    // The department in which the student is enrolled
    String dept;

    // The grade of the student (e.g., A, B, C)
    char grade;

    // The CGPA (Cumulative Grade Point Average) of the student, represented as a byte
    float cgpa;
}

