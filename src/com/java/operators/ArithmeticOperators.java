package com.java.operators;

public class ArithmeticOperators {
    public static void main(String[] args) {
        // Initialize two integer variables
        int numberOne = 200;
        int numberTwo = 20;

        // Print a header for arithmetic operations
        System.out.println("-----Arithmetic Operators-----");

        // Perform and print the result of addition
        System.out.println("Addition :" + (numberOne + numberTwo)); 

        // Perform and print the result of subtraction
        System.out.println("Subtraction :" + (numberOne - numberTwo));

        // Perform and print the result of multiplication
        System.out.println("Multiplication :" + (numberOne * numberTwo));

        // Perform and print the result of division
        System.out.println("Division :" + (numberOne / numberTwo));

        // Perform and print the result of modulus (remainder)
        System.out.println("Module(Remainder) :" + (numberOne % numberTwo));
    }
}
