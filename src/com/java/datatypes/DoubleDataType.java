package com.java.datatypes;

public class DoubleDataType {
    // This is the main method where the program starts running.
    public static void main(String[] args) {
        // Create a double variable called 'myMarks' and set it to 23.5.
        // 'double' is used for numbers with decimal points and doesn't need a suffix.
        double myMarks = 23.5;

        // Print the value of 'myMarks' to the screen.
        // The '+' is used to combine the text with the value of 'myMarks'.
        System.out.println("My Subject Marks: " + myMarks);
    }
}
