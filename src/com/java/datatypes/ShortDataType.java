package com.java.datatypes;

public class ShortDataType {
    // The main method is the entry point for the Java application.
    public static void main(String[] args) {
        // Declare a short variable named 'employeeSalary' and initialize it with the value 30000.
        short employeeSalary = 30000;

        // Print out the value of 'employeeSalary' to the console.
        // The '+' operator is used to concatenate the string with the value of 'employeeSalary'.
        System.out.println("Employee Salary: " + employeeSalary);
    }
}
