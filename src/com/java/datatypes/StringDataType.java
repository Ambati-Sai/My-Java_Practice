package com.java.datatypes;

public class StringDataType {
    // This is the main method where the program starts running.
    public static void main(String[] args) {
        // Create a String variable called 'studentName' and set it to "Ambati Sai".
        // 'String' is used for text and must be enclosed in double quotes.
        String studentName = "Ambati Sai";

        // Print the value of 'studentName' to the screen.
        // The '+' is used to combine the text with the value of 'studentName'.
        System.out.println("My Name is: " + studentName);
    }
}

