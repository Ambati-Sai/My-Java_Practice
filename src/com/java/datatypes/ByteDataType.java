package com.java.datatypes;

public class ByteDataType {
    // The main method is the entry point for the Java application.
    public static void main(String[] args) {
        // Declare a byte variable named 'orderNumber' and initialize it with the value 127.
        byte orderNumber = 127;

        // Print out the value of 'orderNumber' to the console.
        // The '+' operator is used to concatenate the string with the value of 'orderNumber'.
        System.out.println("Your orderNumber is: " + orderNumber);
    }
}
