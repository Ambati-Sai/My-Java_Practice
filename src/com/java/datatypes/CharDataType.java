package com.java.datatypes;

public class CharDataType {
    // This is the main method where the program starts running.
    public static void main(String[] args) {

        // 'char' is used for single characters and must be enclosed in single quotes.
        char firstAlphabet = 'A';

        // Print the value of 'firstAlphabet' to the screen.
        // The '+' is used to add the text with the value of 'firstAlphabet'.
        System.out.println("First Alphabet Letter is: " + firstAlphabet);
    }
}
