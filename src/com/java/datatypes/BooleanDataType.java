package com.java.datatypes;

public class BooleanDataType {
    // This is the main method where the program starts running.
    public static void main(String[] args) {
        // Create a boolean variable called 'ifYes' and set it to true.
        // 'boolean' is used for true or false values.
        boolean ifYes = true;

        // Create a boolean variable called 'ifNo' and set it to false.
        boolean ifNo = false;

        // Print the value of 'ifYes' to the screen.
        // The '+' is used to combine the text with the value of 'ifYes'.
        System.out.println("Your name is correct: " + ifYes);

        // Print the value of 'ifNo' to the screen.
        // The '+' is used to combine the text with the value of 'ifNo'.
        System.out.println("Your name is incorrect: " + ifNo);
    }
}
