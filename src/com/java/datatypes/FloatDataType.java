package com.java.datatypes;

public class FloatDataType {
    // This is the main method where the program starts running.
    public static void main(String[] args) {
        // Create a float variable called 'height' and set it to 5.75.
        // The 'f' at the end tells Java this number is a float, not a double.
        float height = 5.75f;

        // Print the height value to the screen.
        // The '+' is used to add the text with the value of 'height'.
        System.out.println("Your Height: " + height);
    }
}
