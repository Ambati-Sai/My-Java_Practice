package com.java.datatypes;

public class SumOperation {
    // This is the main method where the program starts running.
    public static void main(String[] args) {
        // Create an int variable called 'firstNumber' and set it to 100.
        int firstNumber = 100;

        // Create an int variable called 'secondNumber' and set it to 80.
        int secondNumber = 80;

        // Calculate the sum of 'firstNumber' and 'secondNumber' and store it in 'sum'.
        int sum = firstNumber + secondNumber;

        // Print the value of 'sum' to the screen.
        // The '+' is used to combine the text with the value of 'sum'.
        System.out.println("Sum of two numbers is: " + sum);
    }
}
