package com.java.oopsclassesandobjects;

public class InventoryItem {
    // Instance variable to store the name of the item
    String itemName;

    // Instance variable to store a unique code for the item
    String itemCode;

    // Instance variable to store the quantity of the item in stock
    int quantity;

    // Instance variable to store the price of the item
    int itemPrize;

    // Instance variable to store the category of the item
    String itemCategory;

}

