package com.java.controlstatements;

public class ElseExample {
    public static void main(String[] args) {

        int numberOne = 100;

        int numberTwo = 200;

        // Check if numberOne is greater than numberTwo
        if (numberOne > numberTwo) {
            // Print a message if numberOne is greater than numberTwo
            System.out.println("Number one is greater than Number Two");
        } else {
            // Print a message if numberOne is not greater than numberTwo
            System.out.println("NumberOne is less than Number Two");
        }
    }
}
